export const getProductById = (id: number): Product | undefined => {
  return products.find((p) => p.id === id);
};

export const getBestProducts = (): Product[] => {
  return products.filter((p) => p.isBest);
};

export const getHotProducts = (): Product[] => {
  return products.filter((p) => p.isHot);
};

export const getProductsByBrand = (brand: string): Product[] => {
  return products.filter((p) => p.brand === brand);
};

export const filterProducts = (
  filters: {
    brand?: string[];
    carrier?: string[];
    subscriptionType?: string[];
    priceRange?: { min: number; max: number };
  }
): Product[] => {
  return products.filter((p) => {
    if (filters.brand?.length && !filters.brand.includes(p.brand)) return false;
    if (filters.carrier?.length && !p.carrier.some((c) => filters.carrier?.includes(c))) return false;
    if (filters.subscriptionType?.length && !p.subscriptionType.some((s) => filters.subscriptionType?.includes(s))) return false;
    if (filters.priceRange) {
      if (p.finalPrice < filters.priceRange.min || p.finalPrice > filters.priceRange.max) return false;
    }
    return true;
  });
};
